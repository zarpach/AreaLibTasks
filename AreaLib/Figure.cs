namespace AreaLib
{
    /// <summary>
    /// Represents an abstract geometric figure with a method to calculate its area.
    /// </summary>
    public abstract class Figure
    {
        /// <summary>
        /// Calculates the area of the figure.
        /// </summary>
        /// <returns>The area of the figure as a double.</returns>
        public abstract double GetArea();
    }
}
